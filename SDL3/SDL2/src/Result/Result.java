    public Result(Statement s,String tablename) {
        this.s=s;
        this.tablename=tablename;
        String sql = "select candidatename,vote from "+tablename;
        this.s=s;
        ResultSet result= null;
        try {
            result = s.executeQuery(sql);
            ArrayList<String> arrayList=new ArrayList<>();

            while(result.next()){
                arrayList.add(result.getString(1)+"        "+result.getInt(2));
            }
            strings=new String[arrayList.size()];
            vote=new int[arrayList.size()];
            int j=0;
            for(int i=arrayList.size()-1;i>-1;i--)
            {
                strings[j]=arrayList.get(i);
                j++;
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }        initComponents();
    }

