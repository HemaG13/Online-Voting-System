    private void elistMouseClicked(java.awt.event.MouseEvent evt) {

        // TODO add your handling code here:
        JList list = (JList)evt.getSource();
        if (evt.getClickCount() == 2) {
            int index = list.locationToIndex(evt.getPoint());
            System.out.println("index="+index);
            String str[]=strings[index].split("<");
            str[1]=str[1].substring(0, str[1].length() - 1);
            try {

                Date date1=new SimpleDateFormat("yyyy-MM-dd").parse(str[1]);
                Date date2=new Date();

                if(date1.getYear()==date2.getYear()&&date1.getMonth()==date2.getMonth()&&date1.getDate()==date2.getDate())
                {
                    String tablename= str[0].substring(0,str[0].length()-1)+str[1];
                    tablename=tablename.replace('-','_');
                    String sql="select * from "+tablename+"voting where username='"+username+"'";
                    ResultSet r=s.executeQuery(sql);
                    if(!r.next())
                    {
                        java.awt.EventQueue.invokeLater(new Runnable() {
                            public void run() {
                                String tablename= str[0].substring(0,str[0].length()-1)+str[1];
                                tablename=tablename.replace('-','_');
                                new VotingList(s,tablename,username).setVisible(true);
                            }
                        });
                    }else {
                        JOptionPane.showMessageDialog(this, "You already voted for this!");
                    }

                }
                else{
                    JOptionPane.showMessageDialog(this, "Election is not on Today!");

                }
            } catch (ParseException e) {
                e.printStackTrace();
            } catch (SQLException e) {
                e.printStackTrace();
            }

        }
    }

