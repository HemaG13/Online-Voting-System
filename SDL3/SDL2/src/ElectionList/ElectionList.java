    public ElectionList(Statement s,String username) {
        this.username=username;
        System.out.println("username="+this.username);
        this.s=s;

        String sql = "select ELECTION_NAME,DATE from new_election";
        ResultSet result= null;
        try {
            result = s.executeQuery(sql);
            ArrayList<String>arrayList=new ArrayList<>();
            while(result.next()){
//                 strings = new String[]{"Item 1", "Item 2", "Item 3", "Item 4", "Item 5"};
                arrayList.add(result.getString(1)+" <"+result.getString(2)+">");
            }
            strings=new String[arrayList.size()];
            int j=0;
            for(int i=arrayList.size()-1;i>-1;i--)
            {
                strings[j]=arrayList.get(i);
                j++;
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }


        initComponents();

    }

